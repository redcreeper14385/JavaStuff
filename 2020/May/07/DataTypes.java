public class DataTypes {
	public static void main(String[] args) {
    int myNum = 5; // Declares an integer
    float myFloatNum = 5.99f; // Declares a float
    char myLetter = 'D'; // Declares a character
    boolean myBool = true; // Declares a boolean value
    String myText = "Hello"; // Declares a string
    System.out.println("My number is " + myNum);
    System.out.println("My float number is " + myFloatNum);
    System.out.println("My character is " + myLetter);
    System.out.println("My boolean value is " + myBool);
    System.out.println("My string is " + myText);
    // The lines above print out the different variables.
	}
}
