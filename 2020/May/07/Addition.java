public class Addition {
	public static void main(String[] args) {
    int sum1 = 100 + 50; // Adds two numbers.
    int sum2 = sum1 + 250; // Adds a variable and a number.
    int sum3 = sum2 + sum2; // Adds two variables.
    System.out.println(sum1); // 150 (100 + 50)
    System.out.println(sum2); // 400 (150 + 250)
    System.out.println(sum3); // 800 (400 + 400)
	}
}
