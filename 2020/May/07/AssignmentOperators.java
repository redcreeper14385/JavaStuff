public class AssignmentOperators {
	public static void main(String[] args) {
    int x = 10; // Assigns 10 to the variable x
    x += 3; // Adds 3 to the variable x
    x -= 3; // Subtracts 3 from the variable x
    x *= 3; // Multiplies the variable x by 3
    x /= 3; // Divides the variable x by 3
    x %= 3; // Gives the division remainder of x and 3    
	}
}
