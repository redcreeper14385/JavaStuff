public class ArithmeticOperators {
	public static void main(String[] args) {
    int x = 50;
    int y = 13;
    int add = x + y; // Adds together two values
    int sub = x - y; // Subtracts one value from another
    int multiply = x * y; // Multiplies two values
    int divide = x / y; // Divides one value by another
    int modulus = x % y; // Returns the division remainder
    int increment = ++x; // Increases the value of a variable by 1
    int decrement = --x; // Decreases the value of a variable by 1
	}
}
