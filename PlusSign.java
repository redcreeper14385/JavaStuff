public class PlusSign {
	public static void main(String[] args) {
    String name = "John";
    System.out.println("Hello " + name);
    // The code above adds the string "Hello " to the variable name and prints it.
    String firstName = "John ";
    String lastName = "Doe";
    String fullName = firstName + lastName;
    System.out.println(fullName);
    // The code above adds the variables firstName and lastName together in the variable fullName, before printing it.
    int x = 5;
    int y = 6;
    System.out.println(x + y);
    // This code uses + as a mathematical operator, adding the integers x and y together before printing the result.
	}
}
